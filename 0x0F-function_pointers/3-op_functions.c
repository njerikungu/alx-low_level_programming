#include "3-calc.h"

/**
 * op_add - add two numbers
 * @a: num1
 * @b: num2
 * Return: sum
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two numbers
 * @a: num1
 * @b: num2
 * Return: subtraction
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divides two numbers
 * @a: num1
 * @b: num2
 * Return: division
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mul - multiplies two numbers
 * @a: num1
 * @b: num2
 * Return: product
 */

int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_mod - gives remainder
 * @a: num1
 * @b: num2
 * Return: remainder
 */

int op_mod(int a, int b)
{
	return (a % b);
}
